def calculate_absolute_target(current_angle, target_x, target_y):
    # 将角度转换为弧度
    angle_rad = math.radians(current_angle)
    
    # 计算目标点在飞行器朝向下的相对坐标
    relative_target_x = target_x * math.cos(angle_rad) - target_y * math.sin(angle_rad)
    relative_target_y = target_x * math.sin(angle_rad) + target_y * math.cos(angle_rad)
    
    # 计算绝对目标点的坐标
    absolute_target_x = relative_target_x
    absolute_target_y = relative_target_y
    
    return absolute_target_x, absolute_target_y

